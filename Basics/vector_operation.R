# Arithmatics
v1 <- c(1,2,3)
v2 <- c(5,6,7)

# Element by Element 
print(v1 + v2)
print(v1 - v2)
print(v1 * v2)
print(v1 / v2)

# Sum of a vector
sum(v1)
sum.of.vec <- sum(v1)
print(sum.of.vec)

# Mean,Standard deviation, min, max  of a vector
mean(v1)
sd(v1)
max(v1)
min(v1)

# product of a vector
prod(v1)
prod(v2)
